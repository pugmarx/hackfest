package org.hib.sample;

public class GRAuditException extends Exception {

	private static final long serialVersionUID = 3581166177013953473L;

	public GRAuditException() {
		super();
	}

	public GRAuditException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public GRAuditException(String arg0) {
		super(arg0);
	}

	public GRAuditException(Throwable arg0) {
		super(arg0);
	}

}
